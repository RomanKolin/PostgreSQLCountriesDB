DROP TABLE IF EXISTS Country CASCADE; 
DROP TABLE IF EXISTS Population CASCADE; 
DROP TABLE IF EXISTS Continent CASCADE; 
DROP TABLE IF EXISTS Ocean CASCADE;
DROP TABLE IF EXISTS "Continent and ocean" CASCADE; 
