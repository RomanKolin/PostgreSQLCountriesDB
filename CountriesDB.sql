CREATE TABLE Population(ID serial, nam varchar(25) PRIMARY KEY, siz double precision);
CREATE TABLE Continent(ID serial, nam varchar(15) PRIMARY KEY, area double precision, mount varchar(500));
CREATE TABLE Ocean(ID serial, nam varchar(10) PRIMARY KEY, area double precision, sea varchar(500));
CREATE TABLE "Continent and ocean"(ID serial PRIMARY KEY, cont varchar(15), ocean varchar(10));
ALTER TABLE Country ADD FOREIGN KEY(populat) REFERENCES Population(nam);
ALTER TABLE Country ADD FOREIGN KEY(cont) REFERENCES Continent(nam);
ALTER TABLE "Continent and ocean" ADD FOREIGN KEY(cont) REFERENCES Continent(nam);
ALTER TABLE "Continent and ocean" ADD FOREIGN KEY(ocean) REFERENCES Ocean(nam);
